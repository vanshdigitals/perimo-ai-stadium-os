/** Translate a key using current language, falls back to English */
export function t(key: string, lang: string): string {
  const map = TRANSLATIONS[lang as LangCode] || TRANSLATIONS.en;
  return map[key] ?? TRANSLATIONS.en[key] ?? key;
}

